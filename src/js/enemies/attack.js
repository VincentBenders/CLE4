

class Attack {

    //Properties
    damage;
    hits;
    stunnedHits;
    duration;
    windupDuration;
    animation;



    constructor(damage, hits, stunnedHits, duration, windupDuration, animation) {

        this.damage = damage;
        this.hits = hits;
        this.stunnedHits = stunnedHits;
        this.duration = duration;
        this.windupDuration = windupDuration;
        this.animation = animation;


    }





}